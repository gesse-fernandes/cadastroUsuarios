    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            String nome = txtNome.getText();
            int idade = Integer.parseInt(txtIdade.getText());
            int choice = cbSexo.getSelectedIndex();
            if (choice == 0) {
                JOptionPane.showMessageDialog(null, "Nome: " + nome + "\n Idade: " + String.valueOf(idade) + "\n Sexo: M", "Resultado", JOptionPane.OK_OPTION);
                txtNome.setText("");
                txtIdade.setText("");
                cbSexo.setSelectedIndex(0);
            } else if (choice == 1) {
                JOptionPane.showMessageDialog(null, "Nome: " + nome + "\n Idade: " + String.valueOf(idade) + "\n Sexo: F", "Resultado", JOptionPane.PLAIN_MESSAGE);
                txtNome.setText("");
                txtIdade.setText("");
                cbSexo.setSelectedIndex(0);
            }
        } catch (Exception e) {
            txtNome.setText("");
            txtIdade.setText("");
            cbSexo.setSelectedIndex(0);
            JOptionPane.showMessageDialog(null, "Erro!", "ERRO!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

